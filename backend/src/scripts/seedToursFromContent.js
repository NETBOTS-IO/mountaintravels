const allTours = [...trekkingTours, ...expeditionTours, ...culturalTours];

// ============================================================
// SEED FUNCTION
// ============================================================
async function seedTours() {
  try {
    console.log("🔌 Connecting to MongoDB...");
    await mongoose.connect(process.env.MONGODB_URI);
    console.log("✅ MongoDB connected\n");

    let created = 0;
    let updated = 0;

    for (const tourData of allTours) {
      const { name, ...rest } = tourData;

      // Upsert by name — update if exists, create if not
      const result = await Tour.findOneAndUpdate(
        { name },
        { $set: { name, ...rest } },
        {
          upsert: true,
          new: true,
          runValidators: true,
          setDefaultsOnInsert: true,
        },
      );

      if (result.isNew || result.__v === 0) {
        created++;
        console.log(`  ✅ Created: ${name}`);
      } else {
        updated++;
        console.log(`  🔄 Updated: ${name}`);
      }
    }

    console.log("\n========================================");
    console.log(`🎉 Seeding complete!`);
    console.log(`   ✅ Created: ${created} tours`);
    console.log(`   🔄 Updated: ${updated} tours`);
    console.log(`   📊 Total:   ${allTours.length} tours processed`);
    console.log("========================================\n");

    process.exit(0);
  } catch (error) {
    console.error("\n❌ Seeding failed:", error.message);
    if (error.errors) {
      Object.keys(error.errors).forEach((key) => {
        console.error(`   Field: ${key} — ${error.errors[key].message}`);
      });
    }
    process.exit(1);
  }
}

seedTours();
